namespace MoviesCatalog.Models
{
    public enum RateType
    {
        Poor = 1,   // The movie is of very low quality and not enjoyable.
        Fair,       // The movie has some redeeming qualities but falls short in many aspects.
        Good,       // The movie is enjoyable and entertaining, but may have some flaws.
        VeryGood,   // The movie is of high quality, well-made, and highly enjoyable.
        Excellent   // The movie is exceptional, with outstanding performances, writing, direction, and overall impact.
    }
}
